package common.bean;

public class GoodsBean {

	private int gNo;//商品编号
	private String gId;//商品代号
	private String gType;//商品类型
	private String gColor;//商品颜色
	private String gSize;//商品尺码
	private int gNum;//商品库存
	private String gPrice;//商品价格
	private String gSale;//商品折扣
	
	public int getgNo() {
		return gNo;
	}
	public void setgNo(int gNo) {
		this.gNo = gNo;
	}
	public String getgId() {
		return gId;
	}
	public void setgId(String gId) {
		this.gId = gId;
	}
	public String getgType() {
		return gType;
	}
	public void setgType(String gType) {
		this.gType = gType;
	}
	public String getgColor() {
		return gColor;
	}
	public void setgColor(String gColor) {
		this.gColor = gColor;
	}
	public String getgSize() {
		return gSize;
	}
	public void setgSize(String gSize) {
		this.gSize = gSize;
	}
	public int getgNum() {
		return gNum;
	}
	public void setgNum(int gNum) {
		this.gNum = gNum;
	}
	public String getgPrice() {
		return gPrice;
	}
	public void setgPrice(String gPrice) {
		this.gPrice = gPrice;
	}
	public String getgSale() {
		return gSale;
	}
	public void setgSale(String gSale) {
		this.gSale = gSale;
	}
	
	
	
}
