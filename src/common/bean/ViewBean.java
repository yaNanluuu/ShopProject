package common.bean;

public class ViewBean {
	private int gNo; //商品编号
	private String gId; //商品代号
	private String gColor; //商品颜色
	private String gType; //商品类别
	private String gPrice;//商品单价
	private int num; // 销售数量
	private String orState; //订单状态
	
	public int getgNo() {
		return gNo;
	}
	public void setgNo(int gNo) {
		this.gNo = gNo;
	}
	public String getgId() {
		return gId;
	}
	public void setgId(String gId) {
		this.gId = gId;
	}
	public String getgColor() {
		return gColor;
	}
	public void setgColor(String gColor) {
		this.gColor = gColor;
	}
	public String getgType() {
		return gType;
	}
	public void setgType(String gType) {
		this.gType = gType;
	}
	public String getgPrice() {
		return gPrice;
	}
	public void setgPrice(String gPrice) {
		this.gPrice = gPrice;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getOrState() {
		return orState;
	}
	public void setOrState(String orState) {
		this.orState = orState;
	}
	
	

}
